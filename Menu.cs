        private void button1_Click(object sender, EventArgs e)
        {
            string Name = textBox1.Text;
            string query = "SELECT [Код напитка], Название, Объем, Стоимость, Состав FROM Меню WHERE  Название LIKE '%" + Name + "%' ";
            OleDbDataAdapter command = new OleDbDataAdapter(query, myConnection);
            DataTable dt = new DataTable();
            command.Fill(dt);
            dataGridView1.DataSource = dt;
            myConnection.Close();
            textBox1.Clear();
        }



        private void button3_Click(object sender, EventArgs e)
        {
            myConnection = new OleDbConnection(connectString);
            myConnection.Open();
            dataGridView1.DataSource = менюBindingSource;
        }

        private void button4_Click(object sender, EventArgs e)
        {
            int kod = Convert.ToInt32(textBox2.Text);
            string query = "DELETE FROM Меню WHERE [Код Напитка] = " + kod;
            OleDbCommand command = new OleDbCommand(query, myConnection);
            command.ExecuteNonQuery();
            MessageBox.Show("Данные обновлены!");
            this.менюTableAdapter.Fill(this.aRM_KafeDataSet.Меню);
            textBox2.Clear();
        }

        private void Menu_Activated(object sender, EventArgs e)
        {
            this.менюTableAdapter.Fill(this.aRM_KafeDataSet.Меню);
        }
